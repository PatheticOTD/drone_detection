def set_run_font(run, size_pt=14, bold=False, italic=False, name="Times New Roman"):
    run.font.name = name
    run.font.size = Pt(size_pt)
    run.font.bold = bold
    run.font.italic = italic
    r = run._r
    rPr = r.get_or_add_rPr()
    rFonts = OxmlElement('w:rFonts')
    rFonts.set(qn('w:ascii'), name)
    rFonts.set(qn('w:hAnsi'), name)
    rFonts.set(qn('w:cs'), name)
    rPr.insert(0, rFonts)


def set_paragraph_format(para, first_line_cm=1.25, space_before=0, space_after=0,
                          line_spacing=1.5, align=WD_ALIGN_PARAGRAPH.JUSTIFY):
    pf = para.paragraph_format
    pf.first_line_indent = Cm(first_line_cm)
    pf.space_before = Pt(space_before)
    pf.space_after = Pt(space_after)
    pf.line_spacing = Pt(line_spacing * 14)
    pf.alignment = align


def add_page_break_before(para):
    pPr = para._p.get_or_add_pPr()
    pb = OxmlElement('w:pageBreakBefore')
    pb.set(qn('w:val'), '1')
    pPr.append(pb)


def body(doc, text, first_line=1.25):
    para = doc.add_paragraph()
    set_paragraph_format(para, first_line_cm=first_line)
    run = para.add_run(text)
    set_run_font(run)
    return para


def heading1(doc, text, page_break=True):
    para = doc.add_paragraph()
    if page_break:
        add_page_break_before(para)
    para.paragraph_format.space_before = Pt(0)
    para.paragraph_format.space_after = Pt(14)
    para.paragraph_format.alignment = WD_ALIGN_PARAGRAPH.CENTER
    para.paragraph_format.first_line_indent = Cm(0)
    run = para.add_run(text.upper())
    set_run_font(run, bold=True)
    return para


